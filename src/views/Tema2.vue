          .col-12
            img.img-t.img-a(src='@/assets/ilustraciones/img53.svg', alt='')
          .col-8.mt-4
            p Impactos de la Segunda Guerra Mundial (1940-1950)
            p 
              span(style='color:#BF3976') La segunda guerra mundial 
              span(style='color:#00223D') afecta severamente la vida cotidiana de todos los países, las 
              span(style='color:#BF3976') casas de moda 
              span(style='color:#00223D') se ven 
              span(style='color:#BF3976') forzadas a cerrar
              span(style='color:#00223D') , se impone en las mujeres el estilo militar siendo mal visto ostentar riquezas, se decreta por parte del gobierno de Estados Unidos de América la 
              span(style='color:#BF3976') ley de uniformidad de la moda
              span(style='color:#00223D') , en la cual se dictan estrictos lineamientos para el 
              span(style='color:#BF3976') desarrollo de prendas de vestir austeras 
              span(style='color:#00223D') (Geringer et al., 2001; Lehnert, 2000).

        .row.justify-content-center.align-items-center(titulo="1950-1960")
          .col-12
            img.img-t.img-a(src='@/assets/ilustraciones/img54.svg', alt='')
          .col-8.mt-4
            p La postguerra de la Segunda Guerra Mundial (1950-1960)
            p Una vez finalizada la guerra se respira un aire de confianza y tranquilidad, 
              span(style='color:#BF3976') la moda por primera vez en la historia ofrece un abanico de posibilidades para los diferentes tipos de personalidades, actividades y clases sociales
              span(style='color:#00223D') . El #[i New Look] de Cristian Dior se populariza entre las mujeres, luciendo nuevamente glamurosas y femeninas, de igual manera se utilizan todo tipo de pantalones y mezclas entre prendas de vestir de acuerdo a la ocasión de uso. La moda de los jóvenes es influenciada por los artistas de la pantalla grande, los hombres utilizan trajes más desestructurados y frescos (Cárdenas, 2017; Geringer et al., 2001; Lehnert, 2000).

        .row.justify-content-center.align-items-center(titulo="1960-1970")
          .col-12
            img.img-t.img-a(src='@/assets/ilustraciones/img55.svg', alt='')
          .col-8.mt-4
            p Años 60’s. (1960-1970)
            p 
              span(style='color:#BF3976') La moda 
              span(style='color:#00223D') pasa de ser estilista a ser un fenómeno de masas, es decir, 
              span(style='color:#BF3976') al alcance de todos
              span(style='color:#00223D') . La televisión y el cine se convierten en influenciadores y democratizadores de la información, dejando ver los primeros avistamientos de la moda rápida y globalizada. Los jóvenes lideran los 
              span(style='color:#BF3976') movimientos sociales y de moda
              span(style='color:#00223D') , se ve un drástico cambio de roles, los sexos entremezclados dificultaron distinguir entre hombres y mujeres, apareciendo el término 
              span(style='color:#BF3976') androginia. #[i Los hippies]
              span(style='color:#00223D') : movimientos juveniles pacifistas, que se manifiestan en contra de las rígidas políticas internacionales, 
              span(style='color:#BF3976') con estéticas naturales, austeras y libres
              span(style='color:#00223D') . Contrario a esto, 
              span(style='color:#BF3976') los movimientos futuristas, la ciencia-ficción de la literatura y el cine 
              span(style='color:#00223D') se encargó de vender en las personas la idea de ir a la luna, lo cual se reflejó en la 
              span(style='color:#BF3976') estética futurista 
              span(style='color:#00223D') del momento (Cardenas, 2017; Geringer et al., 2001; Laver, 2005; Lehnert, 2000).
            

