          .col-12
            img.img-t.img-a(src='@/assets/ilustraciones/img56.svg', alt='')
          .col-8.mt-4
            p Años 70’s (1970-1980)
            p La politización de la vida pública, evidente en las luchas contra las carreras armamentistas de las potencias mundiales y el crecimiento de la ecología, en los años 70 dio como resultado 
              span(style='color:#BF3976') la idea de nuevas formas de vida
              span(style='color:#00223D') , en las que se incluyen: drogas, amor libre, las comunas, el feminismo, religiones alternativas y creencias ancestrales, entre otros. Debido a lo anterior, 
              span(style='color:#BF3976') la moda se convierte en un medio democrático para la expresión 
              span(style='color:#00223D') personal, en el que convivieron estilos como: hippies, el brillo del disco, los guerrilleros y la romántica eduardiana. Las estrellas del Rock, el disco y el punk lideraron la moda de los jóvenes (Geringer et al., 2001; Lehnert, 2000; #[i Moda siglo XX - Secretos de la moda], s.f.).


